public class Main {
    public static void main(String[] args) {
        add();
        subtract();
        divide();
        multiply();
    }
    public static void add() {
        int number1 = 2;
        int number2 = 8;

        int result = number2 + number1;

        System.out.println(result);

    }
    public static void subtract() {
        int number1 = 2;
        int number2 = 8;

        int result = number2 - number1;

        System.out.println(result);

    }
    public static void divide() {
        int number1 = 2;
        int number2 = 8;

        int result = number2 / number1;

        System.out.println(result);

    }
    public static void multiply() {
        int number1 = 2;
        int number2 = 8;

        int result = number2 * number1;

        System.out.println(result);

    }
}
